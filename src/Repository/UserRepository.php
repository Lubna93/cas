    public function findLatest(): array
    {
    return $this
        ->createQueryBuilder('User')
        ->orderBy('User.id', 'DESC')
        ->setMaxResults(3)
        ->getQuery()
        ->getResult();
    }

    public function findByNewestQueryBuilder(string $search = null): QueryBuilder
    {
        $queryBuilder = $this
            ->createQueryBuilder('User')
            ->orderBy('User.id', 'DESC')
        ;

        if ($search) {
            $queryBuilder->andWhere('User.nom LIKE :searchTerm OR User.prenom LIKE :searchTerm OR User.uid LIKE :searchTerm')
            ->setParameter('searchTerm', '%'.$search.'%');
        }

        return $queryBuilder;

    }
